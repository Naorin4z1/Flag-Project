; 4. วาดดาว (Star of David) ตรงกลาง

; --------------------------------------------------

; เส้นแนวนอน 1 ของดาว (สามเหลี่ยมหงาย) Y=119 ถึง 120 (หนา 2 px)

mov cx, 2

mov di, 38212       ; (119 * 320) + 132 = 38212

star_h1:

    push cx

    mov cx, 57      ; ความยาวเส้นแนวนอน (X=132 ถึง 188)

    mov al, 1

    rep stosb

    add di, 320-57

    pop cx

    loop star_h1



; เส้นแนวนอน 2 ของดาว (สามเหลี่ยมคว่ำ) Y=79 ถึง 80 (หนา 2 px)

mov cx, 2

mov di, 25412       ; (79 * 320) + 132 = 25412

star_h2:

    push cx

    mov cx, 57

    mov al, 1

    rep stosb

    add di, 320-57

    pop cx
